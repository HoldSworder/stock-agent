/** 首次启动（任务表为空）写入全部种子任务 */
export function seedCronTasksIfEmpty(): void {
  const row = db
    .select({ c: sql<number>`count(*)` })
    .from(schema.scheduledTasks)
    .get();
  if ((row?.c ?? 0) > 0) return;
  for (const s of SEEDS) createTask(s);
  console.log(`[seed] 已写入 ${SEEDS.length} 个定时任务（默认禁用，配置后到 WebUI 启用）`);
}

/**
 * 幂等迁移：把已存在的 DB 任务同步到 OpenClaw 线上逻辑。
 * - 先按 RENAME_MAP 重命名（仅当目标规范名尚不存在，避免与已存在规范任务撞名重复）；
 * - 对每个规范任务：存在则在 prompt 命中 LEGACY_PROMPTS 时覆盖 prompt/cron/timeout/model/description；不存在则按 enabled=false 新建；
 * - 对 DEPRECATE_NAMES：存在且仍为旧种子文案时停用（保留数据，避免与 OpenClaw 双跑）。
 * - 对 REMOVED_NAMES：彻底删除（非战法任务），功能已迁模块定时或主动下线。
 * 用户已自定义过 prompt 的任务（不在 LEGACY_PROMPTS 内）一律不动。
 * 返回受影响（重命名/改写/新建/停用/删除）的任务数。
 */
export function syncCronTasksFromOpenClaw(): number {
  let changed = 0;
  const byName = () => new Map(listTasks().map((t) => [t.name, t]));

  // 1. 按 RENAME_MAP 重命名（仅当新名未被占用）
  let map = byName();
  for (const [oldName, newName] of Object.entries(RENAME_MAP)) {
    const oldTask = map.get(oldName);
    if (!oldTask) continue;
    if (map.has(newName)) continue; // 规范任务已存在，旧任务留待 DEPRECATE/忽略
    updateTask(oldTask.id, { name: newName });
    changed++;
    map = byName();
  }

  // 2. 规范任务 upsert
  for (const seed of SEEDS) {
    const existing = map.get(seed.name);
    if (!existing) {
      createTask(seed);
      changed++;
      continue;
    }
    // 仅当 prompt 仍是已知旧种子/含废弃标记时才覆盖（保护用户自定义）
    if (isOverwritablePrompt(existing.prompt)) {
      updateTask(existing.id, {
        prompt: seed.prompt,
        cronExpr: seed.cronExpr,
        timeoutSec: seed.timeoutSec,
        modelConfig: seed.modelConfig,
        description: seed.description,
      });
      changed++;
    }
  }

  // 3. 停用 OpenClaw 已无的旧任务（保留数据）
  map = byName();
  for (const name of DEPRECATE_NAMES) {
    const t = map.get(name);
    if (t && t.enabled && isOverwritablePrompt(t.prompt)) {
      updateTask(t.id, { enabled: false });
      changed++;
    }
  }

  // 4. 彻底删除已退役的非战法中央任务（功能已迁模块定时或主动下线）
  map = byName();
  for (const name of REMOVED_NAMES) {
    const t = map.get(name);
    if (t && !t.strategyId) {
      deleteTask(t.id);
      changed++;
    }
  }

  return changed;
}
